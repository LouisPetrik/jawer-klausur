
public class ZahlenRunden {
	public static void main(String[] args) {
		double age = 23.5; 
		double age2 = 29.3; 
		// math.ceil rundet immer auf. 
		System.out.println(Math.ceil(age));
		System.out.println(Math.ceil(age2));
		
		// math floor rundet immer ab. 
		System.out.println(Math.floor(age));
		
	}
}
